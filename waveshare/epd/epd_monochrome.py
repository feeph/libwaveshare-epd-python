class EPD_monochrome(EPD_generic):

    def __init__(self, spi_bus: busio.SPI, bsy: digitalio.DigitalInOut, dcs: digitalio.DigitalInOut, rst: digitalio.DigitalInOut, scs: digitalio.DigitalInOut, width: int, height: int):  # noqa: E501
        # initialize parent
        super().__init__(spi_bus=spi_bus, bsy=bsy, dcs=dcs, rst=rst, scs=scs, width=width, height=height)
        print("hardware init (monochrome)")
        # -- hardware initialization (monochrome) --
        self.reset()
        self.read_busy()
        self.send_command(0x12)  # SWRESET
        self.read_busy()
        # driver output control
        self.send_command(0x01, bytearray([0x27, 0x01, 0x00]))
        # data entry mode
        self.send_command(0x11, 0x03)
        self.set_window(0, 0, self.width-1, self.height-1)
        # display update control
        self.send_command(0x21, bytearray([0x00, 0x80]))
        self.set_cursor(0, 0)
        self.read_busy()
        self.set_lut(WS_20_30)
        # initialize frame buffer
        self.buf = bytearray(height * width // 8)
        self.fb = adafruit_framebuf.FrameBuffer(self.buf, width=width, height=height, buf_format=adafruit_framebuf.MHMSB)

    def display(self, canvas: waveshare.canvas.MonochromeCanvas):
        self._display(canvas.buf)

    def display_base(self, canvas: waveshare.canvas.MonochromeCanvas):
        self._display(canvas.buf, canvas.buf)

    def display_partial(self, canvas: waveshare.canvas.MonochromeCanvas):
        self._display_partial(canvas.buf)
